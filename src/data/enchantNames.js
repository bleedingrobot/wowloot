export function isKnownEnchantId(enchantId) {
  const id = Number(enchantId);
  if (!Number.isFinite(id) || id <= 0) {
    return false;
  }

  return Boolean(ENCHANT_NAME_BY_ID[id]);
}

export function getEnchantNameById(enchantId) {
  const id = Number(enchantId);
  if (!Number.isFinite(id) || id <= 0) {
    return "";
  }

  return ENCHANT_NAME_BY_ID[id] || "";
}

export function getEnchantLabel(enchantId) {
  const id = Number(enchantId);
  if (!Number.isFinite(id) || id <= 0) {
    return "";
  }

  const knownName = getEnchantNameById(id);
  return knownName ? `${knownName} (#${id})` : `Enchant #${id}`;
}

export function getMissingEnchantIdsFromCharacters(characters = []) {
  const missing = new Set();

  characters.forEach((character) => {
    const equippedItems = Array.isArray(character?.equippedItems) ? character.equippedItems : [];
    equippedItems.forEach((item) => {
      const id = Number(item?.enchantId || 0);
      if (!Number.isFinite(id) || id <= 0) {
        return;
      }

      if (!isKnownEnchantId(id)) {
        missing.add(id);
      }
    });
  });

  return [...missing].sort((a, b) => a - b);
}
